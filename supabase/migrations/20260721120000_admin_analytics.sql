-- Existing rows establish a baseline without inventing historical transition
-- dates. Accurate lifecycle durations therefore begin with this migration.
insert into public.job_status_history (job_id, from_status, to_status, changed_at)
select j.id, null, j.status, now()
  from public.jobs j
 where not exists (select 1 from public.job_status_history h where h.job_id = j.id);

insert into public.booking_status_history (booking_id, from_status, to_status, changed_at)
select b.id, null, b.status, now()
  from public.bookings b
 where not exists (select 1 from public.booking_status_history h where h.booking_id = b.id);

create or replace function public.capture_marketplace_daily_snapshot()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.marketplace_daily_snapshots (
    snapshot_date, total_users, total_jobs, open_jobs, completed_jobs,
    cancelled_jobs, active_services, active_bookings, captured_at
  ) values (
    (now() at time zone 'Pacific/Auckland')::date,
    (select count(*)::integer from public.profiles),
    (select count(*)::integer from public.jobs),
    (select count(*)::integer from public.jobs where status = 'open'),
    (select count(*)::integer from public.jobs where status = 'completed'),
    (select count(*)::integer from public.jobs where status = 'cancelled'),
    (select count(*)::integer from public.services where is_active),
    (select count(*)::integer from public.bookings where status in (
      'pending', 'quote_sent', 'confirmed', 'in_progress',
      'awaiting_completion', 'cancellation_requested'
    )),
    now()
  )
  on conflict (snapshot_date) do update set
    total_users = excluded.total_users,
    total_jobs = excluded.total_jobs,
    open_jobs = excluded.open_jobs,
    completed_jobs = excluded.completed_jobs,
    cancelled_jobs = excluded.cancelled_jobs,
    active_services = excluded.active_services,
    active_bookings = excluded.active_bookings,
    captured_at = excluded.captured_at;
end;
$$;

revoke all on function public.capture_marketplace_daily_snapshot() from public;
grant execute on function public.capture_marketplace_daily_snapshot() to service_role;

select public.capture_marketplace_daily_snapshot();

select cron.unschedule(jobid)
  from cron.job
 where jobname = 'capture-marketplace-daily-snapshot';

select cron.schedule(
  'capture-marketplace-daily-snapshot',
  '5 12 * * *',
  $$select public.capture_marketplace_daily_snapshot();$$
);
